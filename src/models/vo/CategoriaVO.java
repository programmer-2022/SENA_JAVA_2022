package models.vo;


public class CategoriaVO {
    
    private int id;
    private String name;

    public CategoriaVO() { }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }    
}